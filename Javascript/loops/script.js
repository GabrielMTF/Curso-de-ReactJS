// while = Enquanto

var x = 0;
while(x <= 10) {
    document.write("<br>O valor do x é: " + x); // enquanto x for menor ou igual a 10 excreva o valor de x, acrescentando x++, o X ao final vale 11, porque se ele ainda valesse 10, o while ainda estaria sendo executado.
    
    x++;
}

document.write("<br><br>o valor do x é: " + x + "<br></br>");


// for = para
for (a = 0;a <= 10; a++){
    document.write("<br>O valor do a é: " + a); // para a igual a 0, enquanto a for menor ou igual a 10 realizar a++, o a no final vale 11 pelos mesmos motivos do problema anterior.
}

document.write("<br><br>o valor do a é: " + a);